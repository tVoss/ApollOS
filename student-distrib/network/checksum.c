uint32_t create_crc(uint8_t *data, uint16_t nbytes) {
    uint32_t crc = 0;

    int i;
    for (i = 0; i < nbytes; i++) {
        crc = (crc >> 4) ^ crc_table[(crc ^ (data[i] >> 0)) & 0x0F];  /* lower nibble */
        crc = (crc >> 4) ^ crc_table[(crc ^ (data[i] >> 4)) & 0x0F];  /* upper nibble */
    }

    return crc;
}

uint16_t create_compliment(uint8_t *data, uint16_t nbytes) {
    uint32_t sum = 0;
    uint16_t *words = (uint16_t *) data;
    uint16_t nwords = (nbytes + 1) >> 1;

    int i;
    for (i = 0; i < nwords; i++) {
        sum += 0xFFFF & ((words[i] >> 8) | (words[i] << 8));
    }

    while (sum & 0xFFFF0000) {
        sum = (sum & 0xFFFF) + (sum >> 16);
    }

    return ~sum;
}
